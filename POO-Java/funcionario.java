package POO;

public class funcionario {

	private String nome;
	private int idade;
	private int codigoTrab;
	private double salario;
	private int tempoDeTrab;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public int getCodigoTrab() {
		return codigoTrab;
	}
	public void setCodigoTrab(int codigoTrab) {
		this.codigoTrab = codigoTrab;
	}
	public double getSalario() {
		return salario;
	}
	public void setSalario(double salario) {
		this.salario = salario;
	}
	public int getTempoDeTrab() {
		return tempoDeTrab;
	}
	public void setTempoDeTrab(int tempoDeTrab) {
		this.tempoDeTrab = tempoDeTrab;
	}
	
	
}
